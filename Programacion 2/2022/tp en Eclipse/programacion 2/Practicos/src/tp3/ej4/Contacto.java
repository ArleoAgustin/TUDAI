package tp3.ej4;

import java.time.LocalDate;

public class Contacto {
	
	private String nombre, apellido, direccion, email, ciudad;
	
	private LocalDate fNacimiento;
	private int nroTelefono, edad;
	
	
	public Contacto(String nombre, String apellido, String direccion, int edad, String email, LocalDate fNacimiento,
			int nroTelefono) {

		this.nombre = nombre;
		this.apellido = apellido;
		this.direccion = direccion;
		this.email = email;
		this.edad = edad;
		this.fNacimiento = fNacimiento;
		this.nroTelefono = nroTelefono;
	}
	
	public Contacto(String nombre, String apellido,int nroTelefono, int edad, String ciudad) {

		this.nombre = nombre;
		this.apellido = apellido;
		this.edad = edad;
		this.ciudad = ciudad;
		this.nroTelefono = nroTelefono;
	}
	
	public int getEdad() {
		return edad;
	}



	public void setEdad(int edad) {
		this.edad = edad;
	}



	public String getCiudad() {
		return ciudad;
	}


	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}

	public String getNombre() {
		return nombre;
	}


	public void setNombre(String nombre) {
		this.nombre = nombre;
	}


	public String getApellido() {
		return apellido;
	}


	public void setApellido(String apellido) {
		this.apellido = apellido;
	}


	public String getDireccion() {
		return direccion;
	}


	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}


	public String getEmail() {
		return email;
	}


	public void setEmail(String email) {
		this.email = email;
	}


	public LocalDate getfNacimiento() {
		return fNacimiento;
	}


	public void setfNacimiento(LocalDate fNacimiento) {
		this.fNacimiento = fNacimiento;
	}


	public int getNroTelefono() {
		return nroTelefono;
	}


	public void setNroTelefono(int nroTelefono) {
		this.nroTelefono = nroTelefono;
	}
	
	
	
	
}
